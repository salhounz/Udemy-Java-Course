package UnitOne;

public class ForLoops {

	public static void main(String[] args) {

		/*
		 * Infinite Loop 
		 * for(;;) { 
		 *    System.out.println("Hello"); 
		 * }
		 */

		/*
		 * Utilizing printf vs. println 
		 * 
		 * for(int i = 0; i < 5; i++) {
		 *    System.out.println("Hello"); 
		 * }
		 */

		for (int i = 0; i < 5; i++) {
			System.out.printf("The value of i is: %d\n", i);
		}

	}

}
